const languageListeners = new Set();

export function getLang() {
  const stored = localStorage.getItem(STORAGE_KEY);
  return stored === "en" ? "en" : "vi";
}

export function setLang(lang) {
  const nextLang = lang === "en" ? "en" : "vi";
  localStorage.setItem(STORAGE_KEY, nextLang);
  applyI18n();
  languageListeners.forEach((listener) => listener(nextLang));
}

export function t(key, vars = {}) {
  const lang = getLang();
  const template = MESSAGES[lang]?.[key] ?? MESSAGES.vi?.[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) =>
    vars[name] == null ? "" : String(vars[name])
  );
}

export function onLanguageChange(listener) {
  languageListeners.add(listener);
  return () => languageListeners.delete(listener);
}

export function applyI18n(root = document) {
  document.documentElement.lang = getLang() === "en" ? "en" : "vi";

  const pageTitleKey = document.body?.dataset?.pageTitleKey;
  if (pageTitleKey) {
    document.title = t(pageTitleKey);
  }

  root.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  root.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });

  root.querySelectorAll(".lang-switch-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === getLang());
  });

  const togglePasswordBtn = document.getElementById("togglePasswordBtn");
  const passwordInput = document.getElementById("passwordInput");
  if (togglePasswordBtn && passwordInput) {
    togglePasswordBtn.textContent =
      passwordInput.type === "password" ? t("auth.showPassword") : t("auth.hidePassword");
  }
}

export function initI18n() {
  applyI18n();

  document.addEventListener("click", (event) => {
    const btn = event.target.closest(".lang-switch-btn");
    if (!btn?.dataset?.lang) return;
    setLang(btn.dataset.lang);
  });
}

